#different types of tuples

#1.EMPTY TUPLE
tuple=()
print(tuple)

#2.TUPLE HAVING INTEGERS
tuple=(1,2,3)
print(tuple)

#3.TUPLE WITH MIXED DATA TYPES
tuple=(1,"hello",3.4)
print(tuple)

#4.NESTED TUPLE
tuple=("mouse",[8,6,5,4],(1,2,3))
print(tuple)

#5.TUPLE WITH ONE ELEMENT
a=2,
print(a)
print(type(a))